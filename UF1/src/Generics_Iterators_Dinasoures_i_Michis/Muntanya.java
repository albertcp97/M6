package Generics_Iterators_Dinasoures_i_Michis;

public class Muntanya {
	public int altura;
	public String nom;
	
	public Muntanya(int altura, String nom) {
		super();
		this.altura = altura;
		this.nom = nom;
	}

	@Override
	public String toString() {
		return "Muntanya [altura=" + altura + ", nom=" + nom + "]";
	}
	
	
}
